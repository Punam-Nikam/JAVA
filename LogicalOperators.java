
public class LogicalOperators {
    public static void main(String[] args) {
        
        int x,y;
        x=6;
        y=8;
        boolean z;
        String name="oggy";
        z=x==y;
        System.out.println(z);
        z=x!=y;
        System.out.println(z);
        z=x<=y;
        System.out.println(z);
        z=x>=y;
        System.out.println(z);
        z=name.equals("jack");
        System.out.println(z);
    }
}
