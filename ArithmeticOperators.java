public class ArithmeticOperators {
    public static void main(String[] args) {
        
        int num1,num2,sum;
        num1=5;
        num2=3;
        
        sum=num1+num2;
        System.out.println(sum);

        sum=num1-num2;
        System.out.println(sum);

        sum=num1*num2;
        System.out.println(sum);

        sum=num1/num2;
        System.out.println(sum);

        sum=num1%num2;
        System.out.println(sum);
    }
}
